"""## Resume Enhancement for Average-Graded Resumes

When a resume receives an **Average** grade (score 65-75), the system automatically:
1. **Identifies missing sections** (Summary, Experience, Education, Skills)
2. **Suggests stronger action verbs** (e.g., "did" → "Executed")
3. **Recommends quantitative metrics** (%, numbers, impact)
4. **Generates an enhanced version** with template sections where needed
"""

# ============================================
# CELL: Resume Enhancement Module (for Average-graded resumes)
# ============================================

def enhance_resume(text):
    """
    Enhance a resume that received average grading.
    Returns enhanced version with suggestions and improvements.
    """
    text_lower = text.lower()
    enhancements = []
    enhanced_text = text

    # 1. Check for missing sections
    required_sections = {
        'summary': ['professional summary', 'summary', 'executive summary', 'profile'],
        'experience': ['experience', 'work experience', 'employment', 'professional experience'],
        'education': ['education', 'academic', 'degree', 'qualification'],
        'skills': ['skills', 'technical skills', 'competencies', 'expertise']
    }

    missing_sections = []
    for section_name, keywords in required_sections.items():
        if not any(kw in text_lower for kw in keywords):
            missing_sections.append(section_name)

    if missing_sections:
        enhancements.append(f"ADD: Missing sections - {', '.join(missing_sections)}")

    # 2. Action verb suggestions (weak verbs to replace)
    weak_verbs = {
        'did': 'Executed', 'made': 'Developed', 'helped': 'Supported',
        'worked on': 'Delivered', 'used': 'Leveraged', 'got': 'Achieved',
        'handled': 'Managed', 'did work': 'Implemented'
    }

    action_verb_suggestions = []
    for weak, strong in weak_verbs.items():
        if weak in text_lower:
            action_verb_suggestions.append(f"Replace '{weak}' with '{strong}'")

    if action_verb_suggestions:
        enhancements.append("IMPROVE: " + "; ".join(action_verb_suggestions[:3]))

    # 3. Add quantitative metrics if sparse
    import re
    quant_count = len(re.findall(r'\d+%|\d+\s*(?:x|times|years?|months?)\b', text_lower))
    if quant_count < 2:
        enhancements.append("ADD: Include quantitative achievements (e.g., 'Increased X by 25%', 'Reduced costs by $Y')")

    # 4. Build enhanced resume with template additions
    enhanced_parts = [enhanced_text]

    # Add professional summary template if missing
    if 'summary' in missing_sections or 'profile' in missing_sections:
        summary_template = "\n\nPROFESSIONAL SUMMARY\n" + "-"*40 + "\nResults-driven professional with proven experience. Skilled in [ADD YOUR KEY SKILLS]. Demonstrated success in [ADD KEY ACHIEVEMENT]. Seeking to leverage expertise to [ADD GOAL].\n"
        enhanced_parts.append(summary_template)

    # Add skills section template if missing
    if 'skills' in missing_sections:
        skills_template = "\n\nSKILLS\n" + "-"*40 + "\n• [Technical Skill 1]  • [Technical Skill 2]  • [Technical Skill 3]\n• [Soft Skill 1]  • [Soft Skill 2]\n"
        enhanced_parts.append(skills_template)

    enhanced_text = "".join(enhanced_parts)

    return {
        'enhanced_resume': enhanced_text,
        'suggestions': enhancements,
        'missing_sections': missing_sections
    }

def grade_and_enhance(text):
    """
    Full pipeline: Grade resume, and if average, return enhanced version.
    """
    score, grade, needs_enhancement = grade_resume(text)

    result = {
        'score': score,
        'grade': grade,
        'needs_enhancement': needs_enhancement
    }

    if needs_enhancement and grade == "Average":
        enhancement = enhance_resume(text)
        result['enhanced_resume'] = enhancement['enhanced_resume']
        result['suggestions'] = enhancement['suggestions']
        result['missing_sections'] = enhancement['missing_sections']

    return result

