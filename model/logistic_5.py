def predict_professional_score(text):
    """
    Predict resume score from 0-100 (uses TF-IDF + hand features)
    """
    clean = clean_text(text)
    vec_tfidf = vectorizer.transform([clean])
    vec_hand = extract_hand_features(text)
    vec_combined = hstack([vec_tfidf, vec_hand])
    probs = log_model.predict_proba(vec_combined)[0]
    classes = log_model.classes_
    score_0_100 = sum(p * c * 10 for p, c in zip(probs, classes))
    return round(score_0_100)

def grade_resume(text):
    """
    Grade resume and return (score_0_100, grade_level, needs_enhancement)
    Grade levels: Poor (<65), Average (65-75), Good (75-82), Excellent (>82)
    Average resumes automatically get enhancement.
    """
    score = predict_professional_score(text)
    if score < 65:
        grade = "Poor"
        needs_enhancement = True
    elif score < 75:
        grade = "Average"
        needs_enhancement = True   # Enhance average resumes
    elif score < 82:
        grade = "Good"
        needs_enhancement = False
    else:
        grade = "Excellent"
        needs_enhancement = False
    return score, grade, needs_enhancement

